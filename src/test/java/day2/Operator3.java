package day2;

public class Operator3 {
    public static void main (String[] args) {
        int p = 27;
        int m = 25;

        int sum = p+m;

        System.out.println("Sum: "+sum);

        int sub = p-m;
        System.out.println("Sub: "+sub);

        int div = p/m;
        System.out.println("Div: "+div);

        int mul = p*m;
        System.out.println("Mul: "+mul);

        int per = p%m;
        System.out.println("Per: "+per);



    }
}
